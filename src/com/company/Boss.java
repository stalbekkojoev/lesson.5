package com.company;

public class Boss {
    private int health;
    private int damage;
    private String supers;

    public int getHealth() {
        return health;
    }

    public void setHealth(int health) {
        this.health = health;
    }

    public int getDamage() {
        return damage;
    }

    public void setDamage(int damage) {
        this.damage = damage;
    }

    public String getSupers() {
        return supers;
    }

    public void setSupers(String supers) {
        this.supers = supers;
    }
}
